package org.jdesktop.wonderland.common.messages;

import org.jdesktop.wonderland.common.ExperimentalAPI;

/**
 * Indicates a message that is a response to a previously sent request
 * message
 * @author jkaplan
 */
@ExperimentalAPI
public abstract class ResponseMessage extends Message {
    /**
     * Create a new response to a message with the given ID
     * @param messageID the id of the message to respond to
     */
    public ResponseMessage(MessageID messageID) {
        super (messageID);
    }
}
